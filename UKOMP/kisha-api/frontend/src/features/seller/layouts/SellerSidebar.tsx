export function SellerSidebar({ collapsed }: SellerSidebarProps) {
  const pathname = usePathname();

  return (
    <nav className="flex-1 overflow-y-auto px-2 py-4">
      {menuItems.map((item) => {
        const isActive = pathname === item.href || (item.href !== '/seller' && pathname?.startsWith(item.href));
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`mb-1 flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition ${isActive ? 'bg-emerald-50 text-emerald-700' : 'text-slate-600 hover:bg-slate-50 hover:text-slate-900'} ${collapsed ? 'justify-center' : ''}`}
          >
            <span className="flex h-5 w-5 shrink-0 items-center justify-center">
              <MenuIcon name={item.icon} />
            </span>
            {!collapsed && <span>{item.label}</span>}
          </Link>
        );
      })}
    </nav>
  );
}

